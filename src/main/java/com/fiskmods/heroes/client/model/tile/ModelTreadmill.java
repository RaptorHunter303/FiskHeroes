    public void setRotateAngle(ModelRenderer modelRenderer, float x, float y, float z)
    {
        modelRenderer.rotateAngleX = x;
        modelRenderer.rotateAngleY = y;
        modelRenderer.rotateAngleZ = z;
    }

    private void setRotationAngles(TileEntityTreadmill tileentity)
    {
        int id = tileentity.playerId;
        float speed = 0;

        if (tileentity.getWorldObj() != null)
        {
            World world = tileentity.getWorldObj();

            if (world.getEntityByID(id) instanceof EntityPlayer)
            {
                EntityPlayer player = (EntityPlayer) world.getEntityByID(id);
                speed = ModelHelper.getLimbSwingSpeed(null, SHData.TREADMILL_LIMB_PROGRESS.interpolate(player) / 2, 0, 0, 0, 0, 0, player);
            }
        }

        float f = speed % 1;
        float f1 = 0.7853981633974483F * f;

        track1.setRotationPoint(0.0F, -2.25F, 11.0F);
        track21.setRotationPoint(0.0F, 0.0F, -1.0F);
        setRotateAngle(track21, 0.0F, 0.0F, 0.0F);
        track25.setRotationPoint(0.0F, 0.0F, -1.0F);
        setRotateAngle(track25, 0.7853981633974483F, 0.0F, 0.0F);
        track46.setRotationPoint(0.0F, 0.0F, -1.0F);
        setRotateAngle(track46, 0.0F, 0.0F, 0.0F);
        track1.rotationPointZ -= f;
        track21.rotateAngleX += f1;
        track25.rotateAngleX -= f1;
        track46.rotateAngleX += f1;
    }
